"""
    For Loops in Python
"""

#Iterate over a sequence
things = [1,2,3]
for thing in things:
    pass

#Loop Through a String
for char in "some string":
    pass

#break keyword will exit the loop
#continue keyword will move to the next iterations

#Create a basic range to loop over
for num in range(6):
    pass #Loops from 0 to 5

#Get more creative with the range
for num in range(2,6):
    pass # Loops from 2 to 5

#Or even more
for num in range(2,6,2):
    pass # Loops from 2 to 5 and each increment is by 2 

#Else code block will execute when for loop condition evaluates to false


